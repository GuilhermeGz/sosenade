                            <p class="text-justify">Este projeto surgiu a partir de uma demanda da Pró-Reitoria de Ensino de Graduação (PREG) e da necessidade de preparar os discentes da UFRPE para realizarem o Exame Nacional de Desempenho de Estudantes (ENADE), visto que é uma prova importante para o desenvolvimento da universidade, elevando tanto seu reconhecimento em aspecto nacional e internacional quanto a possibilidade de maior visibilidade para investimentos público e privado. O software foi desenvolvido como uma plataforma online, utilizando a linguagem de programação PHP com a framework Laravel.<br />
                            </p>
                            <p class="text-justify">O sistema conta com recursos que viabilizam a simulação da prova, com simulados que se adaptam com a real necessidade do curso, podendo ser modeladas da forma que o usuário queira, por exemplo, priorizando as disciplinas em que os alunos estão com deficit. Para garantir provas mais consistentes, todas as questões são adicionadas por docentes, separadas pela disciplina, ao banco de dados da universidade. A medida que os alunos concluírem um simulado, os coordenadores receberão estatísticas das respostas, organizadas pela área do conhecimento, para que os mesmos possam conhecer melhor o perfil de aprendizagem dos seus estudantes, e a partir dessa informação definir sua estratégia de atuação.</p>
